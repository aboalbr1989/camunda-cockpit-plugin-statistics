package org.camunda.cockpit.plugin.statistics.dto.process;

import java.util.Date;

public class IncidentInstanceCountDto {

	private String incidentMsg;
	private int messageCount;
	private String incidentType;
	private String activityId;
	private String processKey;
	private String processName;
	private Date createTime;
	
	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public String getIncidentMsg() {
		return incidentMsg;
	}

	public void setIncidentMsg(String incidentMsg) {
		this.incidentMsg = incidentMsg;
	}

	public int getMessageCount() {
		return messageCount;
	}

	public void setMessageCount(int messageCount) {
		this.messageCount = messageCount;
	}

	public String getIncidentType() {
		return incidentType;
	}

	public void setIncidentType(String incidentType) {
		this.incidentType = incidentType;
	}

	public String getActivityId() {
		return activityId;
	}

	public void setActivityId(String activityId) {
		this.activityId = activityId;
	}

	public String getProcessKey() {
		return processKey;
	}

	public void setProcessKey(String processKey) {
		this.processKey = processKey;
	}

	public String getProcessName() {
		return processName;
	}

	public void setProcessName(String processName) {
		this.processName = processName;
	}

}
